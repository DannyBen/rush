## [@bashly-upgrade completions send_completions]
send_completions() {
  echo $'# rush completion                                          -*- shell-script -*-'
  echo $''
  echo $'# This bash completions script was generated by'
  echo $'# completely (https://github.com/dannyben/completely)'
  echo $'# Modifying it manually is not recommended'
  echo $''
  echo $'_rush_completions() {'
  echo $'  local cur=${COMP_WORDS[COMP_CWORD]}'
  echo $'  local compline="${COMP_WORDS[@]:1:$COMP_CWORD-1}"'
  echo $''
  echo $'  case "$compline" in'
  echo $'    \'completions\'*)'
  echo $'      while read; do COMPREPLY+=( "$REPLY" ); done < <( compgen -W "--help -h" -- "$cur" )'
  echo $'      ;;'
  echo $''
  echo $'    \'download\'*)'
  echo $'      while read; do COMPREPLY+=( "$REPLY" ); done < <( compgen -W "--help -h" -- "$cur" )'
  echo $'      ;;'
  echo $''
  echo $'    \'default\'*)'
  echo $'      while read; do COMPREPLY+=( "$REPLY" ); done < <( compgen -W "--help -h" -- "$cur" )'
  echo $'      ;;'
  echo $''
  echo $'    \'remove\'*)'
  echo $'      while read; do COMPREPLY+=( "$REPLY" ); done < <( compgen -W "--help --purge -h -p" -- "$cur" )'
  echo $'      ;;'
  echo $''
  echo $'    \'update\'*)'
  echo $'      while read; do COMPREPLY+=( "$REPLY" ); done < <( compgen -W "--help -h" -- "$cur" )'
  echo $'      ;;'
  echo $''
  echo $'    \'upload\'*)'
  echo $'      while read; do COMPREPLY+=( "$REPLY" ); done < <( compgen -W "--all --help --message -a -h -m" -- "$cur" )'
  echo $'      ;;'
  echo $''
  echo $'    \'config\'*)'
  echo $'      while read; do COMPREPLY+=( "$REPLY" ); done < <( compgen -W "--edit --help -e -h" -- "$cur" )'
  echo $'      ;;'
  echo $''
  echo $'    \'snatch\'*)'
  echo $'      while read; do COMPREPLY+=( "$REPLY" ); done < <( compgen -W "--help -h" -- "$cur" )'
  echo $'      ;;'
  echo $''
  echo $'    \'search\'*)'
  echo $'      while read; do COMPREPLY+=( "$REPLY" ); done < <( compgen -W "--help -h" -- "$cur" )'
  echo $'      ;;'
  echo $''
  echo $'    \'clone\'*)'
  echo $'      while read; do COMPREPLY+=( "$REPLY" ); done < <( compgen -A directory -W "--default --help --ignore --name --shallow --ssh -d -h -i -n -s -w" -- "$cur" )'
  echo $'      ;;'
  echo $''
  echo $'    \'pull\'*)'
  echo $'      while read; do COMPREPLY+=( "$REPLY" ); done < <( compgen -W "--help -h" -- "$cur" )'
  echo $'      ;;'
  echo $''
  echo $'    \'push\'*)'
  echo $'      while read; do COMPREPLY+=( "$REPLY" ); done < <( compgen -W "--all --help --message -a -h -m" -- "$cur" )'
  echo $'      ;;'
  echo $''
  echo $'    \'undo\'*)'
  echo $'      while read; do COMPREPLY+=( "$REPLY" ); done < <( compgen -W "$(rush list -s -a) --help -h" -- "$cur" )'
  echo $'      ;;'
  echo $''
  echo $'    \'copy\'*)'
  echo $'      while read; do COMPREPLY+=( "$REPLY" ); done < <( compgen -W "$(rush list -s -a) --force --help -f -h" -- "$cur" )'
  echo $'      ;;'
  echo $''
  echo $'    \'info\'*)'
  echo $'      while read; do COMPREPLY+=( "$REPLY" ); done < <( compgen -W "$(rush list -s -a) --help -h" -- "$cur" )'
  echo $'      ;;'
  echo $''
  echo $'    \'list\'*)'
  echo $'      while read; do COMPREPLY+=( "$REPLY" ); done < <( compgen -W "--all --help --simple -a -h -s" -- "$cur" )'
  echo $'      ;;'
  echo $''
  echo $'    \'edit\'*)'
  echo $'      while read; do COMPREPLY+=( "$REPLY" ); done < <( compgen -W "$(rush list -s -a) --help -h" -- "$cur" )'
  echo $'      ;;'
  echo $''
  echo $'    \'show\'*)'
  echo $'      while read; do COMPREPLY+=( "$REPLY" ); done < <( compgen -W "$(rush list -s -a) --help -h" -- "$cur" )'
  echo $'      ;;'
  echo $''
  echo $'    \'add\'*)'
  echo $'      while read; do COMPREPLY+=( "$REPLY" ); done < <( compgen -A directory -W "--help -h" -- "$cur" )'
  echo $'      ;;'
  echo $''
  echo $'    \'get\'*)'
  echo $'      while read; do COMPREPLY+=( "$REPLY" ); done < <( compgen -W "$(rush list -s -a) --clone --help -c -h" -- "$cur" )'
  echo $'      ;;'
  echo $''
  echo $'    \'a\'*)'
  echo $'      while read; do COMPREPLY+=( "$REPLY" ); done < <( compgen -A directory -W "--help -h" -- "$cur" )'
  echo $'      ;;'
  echo $''
  echo $'    \'r\'*)'
  echo $'      while read; do COMPREPLY+=( "$REPLY" ); done < <( compgen -W "--help --purge -h -p" -- "$cur" )'
  echo $'      ;;'
  echo $''
  echo $'    \'p\'*)'
  echo $'      while read; do COMPREPLY+=( "$REPLY" ); done < <( compgen -W "--help -h" -- "$cur" )'
  echo $'      ;;'
  echo $''
  echo $'    \'c\'*)'
  echo $'      while read; do COMPREPLY+=( "$REPLY" ); done < <( compgen -W "--edit --help -e -h" -- "$cur" )'
  echo $'      ;;'
  echo $''
  echo $'    \'g\'*)'
  echo $'      while read; do COMPREPLY+=( "$REPLY" ); done < <( compgen -W "$(rush list -s -a) --clone --help -c -h" -- "$cur" )'
  echo $'      ;;'
  echo $''
  echo $'    \'u\'*)'
  echo $'      while read; do COMPREPLY+=( "$REPLY" ); done < <( compgen -W "$(rush list -s -a) --help -h" -- "$cur" )'
  echo $'      ;;'
  echo $''
  echo $'    \'i\'*)'
  echo $'      while read; do COMPREPLY+=( "$REPLY" ); done < <( compgen -W "$(rush list -s -a) --help -h" -- "$cur" )'
  echo $'      ;;'
  echo $''
  echo $'    \'l\'*)'
  echo $'      while read; do COMPREPLY+=( "$REPLY" ); done < <( compgen -W "--all --help --simple -a -h -s" -- "$cur" )'
  echo $'      ;;'
  echo $''
  echo $'    \'s\'*)'
  echo $'      while read; do COMPREPLY+=( "$REPLY" ); done < <( compgen -W "--help -h" -- "$cur" )'
  echo $'      ;;'
  echo $''
  echo $'    \'e\'*)'
  echo $'      while read; do COMPREPLY+=( "$REPLY" ); done < <( compgen -W "$(rush list -s -a) --help -h" -- "$cur" )'
  echo $'      ;;'
  echo $''
  echo $'    *)'
  echo $'      while read; do COMPREPLY+=( "$REPLY" ); done < <( compgen -W "$(rush list -s -a) --help --version -h -v a add c clone completions config copy default download e edit g get i info l list p pull push r remove s search show snatch u undo update upload" -- "$cur" )'
  echo $'      ;;'
  echo $''
  echo $'  esac'
  echo $'} &&'
  echo $'complete -F _rush_completions rush'
  echo $''
  echo $'# ex: filetype=sh'
}